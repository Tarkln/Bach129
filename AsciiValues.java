package day04asciiwwrapperclassoperatorsmemoryusage;

public class AsciiValues {

     //java da her kalan değerin bir sayısal değeri vardır.
     //Bu değerler ASCİİ değerler olarak değerlendirilir.
     // Bu değerlerin tamamının kullanıldığı tabloya ASCİİ Table denir.


    public static void main(String[] args) {

        //herhangi bir character'i ASCII değerini bulmak için o karakteri "int" data type'ında bir variable içine koyunuz.

        int ch = 'A';
        System.out.println(ch);

        int unlem = '!';
        System.out.println(unlem);

        char c1 = 'K';
        char c2 = '?';

        System.out.println(c1+c2);

        int K = 'K';
        System.out.println(K);

    }
}
